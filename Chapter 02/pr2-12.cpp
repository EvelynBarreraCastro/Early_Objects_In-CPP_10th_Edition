// This program uses a char variable and several character literals.
// It shows how letters can have the same application as numbers
// and that it the variable itself can also change as the program runs
#include <iostream>
using namespace std;

int main()
{
   char letter;

   letter = 'A';
   cout << letter << endl;
	
   letter = 'B';
   cout << letter << endl;
   return 0;
}
