// This program uses Boolean variables.
#include <iostream>
using namespace std;

int main()
{
   bool boolValue;

   boolValue = true;
   cout << boolValue << endl;
	
   boolValue = false;
   cout << boolValue << endl;
   return 0;
}

/*
   Note: This program shows the theory of boolean values.
   Existing in a binary state of 1 and 0.
   True = 1 = On
   False = 0 = Off
   
*/